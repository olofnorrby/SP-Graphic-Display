//Välj grafisk display och ettställ de bitar som är 1 i x
void graphic_ctrl_bit_set(uint8_t x){
	uint8_t c = *GPIO_ODR_LOW;
	c &= 0xFB;
	c |= x;
	*GPIO_ODR_LOW = c; 
}

//Välj grafisk display och nollställ de bitar som är 1 i x
void graphic_ctrl_bit_clear(uint8_t x){
	uint8_t c = *GPIO_ODR_LOW;
	c &= 0xFB;
	c &= ~x;
	*GPIO_ODR_LOW = c; 
}

//Konfigurera CS-signalerna
void select_controller(uint8_t controller){
	if (controller == B_CS1){
		graphic_ctrl_bit_set(B_CS1);
		graphic_ctrl_bit_clear(B_CS2);
	}
	if (controller == B_CS2){
		graphic_ctrl_bit_set(B_CS2);
		graphic_ctrl_bit_clear(B_CS1);
	}
	if (controller == (B_CS1 | B_CS2)){
		graphic_ctrl_bit_set(B_CS1 | B_CS2);
	}
	if (controller == 0){
		graphic_ctrl_bit_clear(B_CS1 | B_CS2);
	}
}

//Tidsdiskret *SKRIV* till Grafik-display
void graphic_write(uint8_t value, uint8_t controller){
	*GPIO_ODR_HIGH = value;
	select_controller(controller);
	delay_500ns();
	graphic_ctrl_bit_set(B_E);
	delay_500ns();
	graphic_ctrl_bit_clear(B_E);
	
	if((controller & B_CS1)==1){
		select_controller(B_CS1);
		graphic_wait_ready();
	}
	if((controller & B_CS2)==1){
		select_controller(B_CS2);
		graphic_wait_ready();
	}
	*GPIO_ODR_HIGH = 0x00000000;
	graphic_ctrl_bit_set(B_E);
	select_controller(0);
}

//Skriver *KOMMANDO* till Grafik-display
void graphic_write_command(uint8_t command, uint8_t controller){
	graphic_ctrl_bit_clear(B_E);
	select_controller(controller);
	graphic_ctrl_bit_clear(B_RS | B_RW);
	graphic_write(command, controller);
}

//Skriver *DATA* till Grafik-display
void graphic_write_data(uint8_t datan, uint8_t controller){
	graphic_ctrl_bit_clear(B_E);
	select_controller(controller);
	graphic_ctrl_bit_clear(B_RW);
	graphic_ctrl_bit_set(B_RS);
	graphic_write(datan ,controller);
}

//Läser *1 BYTE* från Grafik-displayens minne
uint8_t graphic_read(uint8_t controller){
	graphic_ctrl_bit_clear(B_E);
	*GPIO_MODER = 0x00005555;
	graphic_ctrl_bit_set(B_RW | B_RS);
	select_controller(controller);
	delay_500ns();
	graphic_ctrl_bit_set(B_E);
	delay_500ns();
	uint8_t rv = *GPIO_IDR_HIGH;
	graphic_ctrl_bit_clear(B_E);
	*GPIO_MODER = 0x55555555;
	if(controller == B_CS1){
		select_controller(B_CS1);
		graphic_wait_ready();
	}
	if(controller == B_CS2){
		select_controller(B_CS2);
		graphic_wait_ready();
	}
	return rv;
}

//Läser *DATA* från Grafik-display
uint8_t graphic_read_data(uint8_t controller){
	(void) graphic_read(controller);
	return graphic_read(controller);
}



//Läser och returnerar då *BUISY* flaggan == 0 
void graphic_wait_ready(void){
	uint8_t rv;
	graphic_ctrl_bit_clear(B_E);
	*GPIO_MODER = 0x00005555;
	graphic_ctrl_bit_clear(B_RS);
	graphic_ctrl_bit_set(B_RW);
	delay_500ns();
	while(1){
		graphic_ctrl_bit_set(B_E);
		delay_500ns();
		graphic_ctrl_bit_clear(B_E);
		rv = *GPIO_IDR_HIGH;
		if((rv & LCD_BUSY) ==0)
			break;
		delay_500ns();
	}
	graphic_ctrl_bit_set(B_E);
	*GPIO_MODER = 0x55555555;
}

void graphic_clear_screen(void){
	for(int page = 0; page<=7; page++){
		graphic_write_command(LCD_SET_PAGE | page, B_CS1|B_CS2);
		graphic_write_command(LCD_SET_ADD | 0 , B_CS1|B_CS2);
		for(int add=0; add<=63; add++){
			graphic_write_data(0, B_CS1|B_CS2);
		}
	}
}

void pixel(unsigned x, unsigned y, unsigned set){
	if(x>127 || y>63)
		return;
	/* Skapar bitmask för pixeln */
	int index = (y-1)/8;
	unsigned int mask;
	switch((y-1)%8){
		case 0:
			mask = 1;
			break;
		case 1:
			mask = 2;
			break;
		case 2:
			mask = 4;
			break;
		case 3:
			mask = 8;
			break;
		case 4:
			mask = 0x10;
			break;
		case 5:
			mask = 0x20;
			break;
		case 6:
			mask = 0x40;
			break;
		case 7:
			mask = 0x80;
			break;
	}
	if (set == 0)
		mask = ~mask;
	/* Bestäm fysiska koordinater och välj styrkrets */
	uint8_t controller, x_fysisk, temp;
	if(x>64){
		controller = B_CS2;
		x_fysisk = x-65;
	}
	else{
		controller = B_CS1;
		x_fysisk = x-1;
	}
	
	graphic_write_command(LCD_SET_ADD | x_fysisk, controller);
	graphic_write_command(LCD_SET_PAGE | index, controller);
	temp = graphic_read_data(controller);		
	graphic_write_command(LCD_SET_ADD | x_fysisk, controller);
	if(set == 1)
		mask = mask | temp;
	else
		mask = mask & temp;
	graphic_write_data(mask, controller);
}

